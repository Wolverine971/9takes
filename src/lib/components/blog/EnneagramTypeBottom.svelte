	<h3>1w9: The Idealist</h3>
	<p>
		The 1's standards plus the 9's calm. Reform happens, but quietly — in policies, systems, and
		long emails rather than confrontations.
	</p>
	<ul>
		<li>More reserved and contemplative than the pure 1</li>
		<li>The internal tension: "I see what's wrong, but I don't want to disrupt"</li>
		<li>The quiet reformer who fixes things behind the scenes</li>
		<li>Looks composed; the standards are still razor-sharp underneath</li>
	</ul>

	<h3>1w2: The Advocate</h3>
	<p>
		The 1's principles plus the 2's warmth. They don't just want things to be right — they want to
		<em>help</em> people get there.
	</p>
	<ul>
		<li>More expressive and people-facing than the pure 1</li>
		<li>Crusader energy for causes that involve real humans, not just abstract right and wrong</li>
		<li>Critical thinking softened by genuine concern for who gets affected</li>
		<li>The teacher, the activist, the doctor with strong opinions</li>
	</ul>

